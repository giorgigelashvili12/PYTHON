# 2D lists = a list of lists, nothing special
drinks = ["water"]
dinner = ["salad", "chicken"]
dessert = ["apple", "mango"]

food = [drinks, dinner, dessert]
print(food[0][0], food[1][1], food[2][1])
# so 2D lists are a full list of seperate lists,
# with it you can access it's elements by using square brackets.