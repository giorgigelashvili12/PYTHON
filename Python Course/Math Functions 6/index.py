import math # imports all math functions
pi = 3.14
print(round(pi)) # 3
# will round a number for us
print(math.ceil(pi)) # 4
# will round a number up
print(math.floor(pi)) # 3
# round a number
print(abs(pi)) # 3.14
# will give absolute number (from decimal to positive)
print(pow(pi, 2)) # 9.8596
# pi = base, 2 = exponet, pi to the power of 2
print(math.sqrt(pi)) # square root of pi