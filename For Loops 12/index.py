# for loop = a statement that will execute it's block of code
# a limited amount of times. for loop can be used over a list,
# tuple, a set, string or a dictionary.

# while loop = unlimited
# for loop = limited
for i in range(10):
    print(i + 1)
# range: how many times should it output
# here, it will output 10 times.
# unlike that, there are other ways to use it
for i in range(50, 100 + 1):
    print(i)
# range(50, 100 + 1)
# start at 50
# go till 100
# since it will stop at 99, add 1 